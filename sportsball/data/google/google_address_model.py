def _create_google_address_model(
    query: str, session: requests_cache.CachedSession, dt: datetime.datetime
) -> AddressModel:
    g = _CACHED_GEOCODES.get(query)
    if g is None:
        logging.warning("Failed to find query: %s", query)
        g = geocoder.google(query, session=session)
        _CACHED_GEOCODES[query] = g
    latitude = g.lat
    longitude = g.lng
    weather_model = None
    tz = "UTC"
    if latitude is not None and longitude is not None:
        tf = TimezoneFinder()
        timezone = tf.timezone_at(lng=longitude, lat=latitude)
        if timezone is not None:
            tz = timezone
        weather_model = create_mutli_weather_model(
            session,
            latitude,
            longitude,
            dt,  # pyright: ignore
            tz,
        )
    return AddressModel(
        city=g.city,
        state=g.state,
        zipcode=g.postal,
        latitude=latitude,
        longitude=longitude,
        housenumber=g.housenumber,
        weather=weather_model,  # pyright: ignore
        timezone=tz,
        country=g.country,
    )


@MEMORY.cache(ignore=["session"])
def _cached_create_google_address_model(
    query: str, session: requests_cache.CachedSession, dt: datetime.datetime
) -> AddressModel:
    return _create_google_address_model(query, session, dt)


def create_google_address_model(
    query: str, session: requests_cache.CachedSession, dt: datetime.datetime
) -> AddressModel:
    """Create address model from google."""
    if not pytest_is_running.is_running() and dt < datetime.datetime.now().replace(
        tzinfo=dt.tzinfo
    ) - datetime.timedelta(days=7):
        return _cached_create_google_address_model(query, session, dt)
    with session.cache_disabled():
        return _create_google_address_model(query, session, dt)
